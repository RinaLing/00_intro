//

#include <iostream>

int main()
{
    int x = 13;
    int y = 73;

    std::cout << " x = " << x << "\n";
    std::cout << " y = " << y << "\n";
    std::cout << " x + y = " << x + y << "\n";
    std::cout << " x - y = " << x - y << "\n";
    std::cout << " x / y = " << x / y << "\n";
    std::cout << " x % y = " << x % y << "\n";

    return 0;
}
